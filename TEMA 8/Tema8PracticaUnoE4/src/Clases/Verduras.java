
package Clases;

public class Verduras {
   private int Zanahorias;
   private int Platanos;
   private int Cebollas;

    public Verduras() {
    }

    public Verduras(int Zanahorias, int Platanos, int Cebollas) {
        this.Zanahorias = Zanahorias;
        this.Platanos = Platanos;
        this.Cebollas = Cebollas;
    }

    public int getZanahorias() {
        return Zanahorias;
    }

    public void setZanahorias(int Zanahorias) {
        this.Zanahorias = Zanahorias;
    }

    public int getPlatanos() {
        return Platanos;
    }

    public void setPlatanos(int Platanos) {
        this.Platanos = Platanos;
    }

    public int getCebollas() {
        return Cebollas;
    }

    public void setCebollas(int Cebollas) {
        this.Cebollas = Cebollas;
    }
    
}
